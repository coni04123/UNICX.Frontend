// Translation function that uses the language context
export function useTranslations(namespace: TranslationNamespace) {
  // This will be updated to use the language context in components
  return (key: string, lang: Language = 'en') => {
    const keys = key.split('.');
    let value: any = translations[lang][namespace];
    
    for (const k of keys) {
      value = value?.[k];
    }
    
    return value || key;
  };
}

